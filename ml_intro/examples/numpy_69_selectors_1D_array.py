# Výběr prvků v poli

# import hlavního balíčku knihovny Numpy
import numpy

# jednorozměrná pole - vektory
a = numpy.arange(12)

# tisk původního pole
print(a)

# indexování prvků od nuly
print(a[0])

# indexování prvků od nuly
print(a[5])

# indexovat lze i od konce pole
print(a[-1])

# indexovat lze i od konce pole
print(a[-5])
