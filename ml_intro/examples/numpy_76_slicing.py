# Slicing - vynechání indexu/indexů

# import hlavního balíčku knihovny Numpy
import numpy

# původní pole
a = numpy.arange(12)

# tisk původního pole
print(a)

# slicing
b = a[:7]

# tisk nového pole
print(b)

# slicing
c = a[5:]

# tisk nového pole
print(c)

# slicing
d = a[:]

# tisk nového pole
print(d)
