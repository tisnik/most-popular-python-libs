# Použití funkce numpy.linspace
#
# zde explicitně specifikujeme, že výsledný vektor má mít jedenáct prvků
# (tím, že se začíná od nuly, získáme krok 0.1)

# import hlavního balíčku knihovny Numpy
import numpy

# konstrukce pole
